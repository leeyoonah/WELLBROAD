package com.wellbroad.db.beans.tour;

public class TourCityDTO {
	String code_city;
	String name;
	String code_nation;
	String description;

	public String getCode_city() {
		return code_city;
	}

	public void setCode_city(String code_city) {
		this.code_city = code_city;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode_nation() {
		return code_nation;
	}

	public void setCode_nation(String code_nation) {
		this.code_nation = code_nation;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}
